<p><strong>虚拟主机是什么？</strong></p>
<p>星空云的虚拟主机共分为三种，分别为EasyPanel，宝塔和MNBT</p>
<p>EasyPanel和MNBT为多个用户的网站共享同一台服务器的环境资源（如CPU、内存、带宽等），仅有存储空间为独享的，适合纯PHP环境的小型网站（如WordPress、Typecho、Discuz等），价格相对较为便宜</p>
<p>宝塔主机为LXD容器，提供完整的宝塔面板以及预装LNMP环境，在此基础上还赋予了SSH权限，用户也可以使用SSH进行连接后部署自己想要的环境，对比前者，此产品的内存以及存储空间是独享的，自由度较高，价格相对较贵</p>
<p><strong>虚拟主机的特点是什么？</strong></p>
<p>对比云服务器，此类产品一般价格相对较低，提供了基础的环境以及简单易用的面板，面对建站新手以及小型站点的托管而言性价比较高</p>
<h2 id="购买指南" tabindex="-1"><a class="header-anchor" href="#购买指南"><span>购买指南</span></a></h2>
<p><strong>我该选哪个地区的虚拟主机？</strong></p>
<p>星空云的虚拟主机主要分为两个地区，分别为香港和美国，下列是它们的优缺点</p>
<p>香港：</p>
<p>1.靠近大陆，线路优化好，响应延迟快</p>
<p>2.带宽较小，适用于图片、视频(大文件)不多的网站</p>
<p>3.无防御，适用于小规模无攻击的站点，大规模攻击建议配合CDN使用</p>
<p>美国：</p>
<p>1.远离大陆，有线路优化，响应延迟较快</p>
<p>2.带宽偏大，适用于图片、视频(大文件)较多的网站</p>
<p>3.有DDos防御，适用于有少量攻击的站点，大规模攻击建议搭配CDN使用</p>
